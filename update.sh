#!/bin/bash

# ==========================================
# 校园爪迹 - 服务器一键强制更新与部署脚本
# ==========================================

info() { echo -e "\033[32m[INFO] $1\033[0m"; }
warn() { echo -e "\033[33m[WARN] $1\033[0m"; }
error() { echo -e "\033[31m[ERROR] $1\033[0m"; exit 1; }

echo "=========================================="
info "开始一键更新服务器代码并部署..."
echo "=========================================="

# 1. 强制拉取远程仓库的最新更改
info "步骤 1: 正在从 GitHub 获取最新代码..."
git fetch origin main --tags || error "无法连接至 GitHub，请检查服务器网络或凭证设置！"

# 2. 强制覆盖本地已追踪文件的修改，解决潜在的冲突
info "步骤 2: 正在强制同步并覆盖本地代码（本地配置如 .local-configs 不受影响）..."
git reset --hard origin/main || error "代码强制同步失败！"

# 3. 赋予部署脚本可执行权限
info "步骤 3: 赋予脚本执行权限..."
chmod +x deploy.sh

# 4. 调起部署流程
info "步骤 4: 调起一键构建与部署程序..."
./deploy.sh
